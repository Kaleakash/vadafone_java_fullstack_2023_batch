package com.service;

public class Operation {

	public int add(int x, int y) {
		int sum = x+y;
		return sum;
	}
	
	public int sub(int x, int y) {
		int sub = x-y;
		return sub;
	}
	
	public int mul(int x, int y) {
		int mul = x*y;
		return mul;
	}
	
	public int div(int x, int y) {
		int div = x/y;
		return div;
	}
}
